    private void txt_logearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_logearActionPerformed
    String vcontraseña, vestado, vusuario, vtipo;
    
    LoginDAO logindao = new LoginDAO();
    Login loginbuscar = new Login();
    loginbuscar.setUsuario(txt_usuario.getText());
    loginbuscar= logindao.query(loginbuscar);
    
        if (loginbuscar.getId()!=null) {
            
            vusuario = loginbuscar.getUsuario();
            vcontraseña = loginbuscar.getContraseña();
            vestado = loginbuscar.getEstado();
            vtipo = loginbuscar.getTipo();
            
            if (vcontraseña.equals(txt_contraseña.getText())&&vestado.equals("1")) {
                try{
                    usr=loginbuscar.getUsuario();
                    tip=vtipo;
                    MDI contenedor = new MDI();
                    contenedor.setVisible(true);
                    this.dispose();
                
                }catch (Exception e) {
                        System.out.println(e);
                }
            }else{
               JOptionPane.showMessageDialog(null, "ERROR AL INICIAR SESIÓN"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, "USUARIO NO REGISTRADO");
        }
    
    
    }//GEN-LAST:event_txt_logearActionPerformed

